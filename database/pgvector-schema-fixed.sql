-- 9. Função de busca semântica (só criar se não existir)
DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM pg_proc 
        WHERE proname = 'search_knowledge'
    ) THEN
        CREATE OR REPLACE FUNCTION search_knowledge(
          query_embedding VECTOR(384),
          match_threshold FLOAT DEFAULT 0.3,
          match_count INT DEFAULT 5
        )
        RETURNS TABLE(
          id BIGINT,
          content TEXT,
          source TEXT,
          category TEXT,
          similarity FLOAT
        )
        LANGUAGE plpgsql
        AS $$
        BEGIN
          RETURN QUERY
          SELECT
            ke.id,
            ke.content,
            ke.source,
            ke.category,
            1 - (ke.embedding <=> query_embedding) AS similarity
          FROM knowledge_embeddings ke
          WHERE 1 - (ke.embedding <=> query_embedding) > match_threshold
          ORDER BY ke.embedding <=> query_embedding
          LIMIT match_count;
        END;
        $$;
    END IF;
END $$;

-- 10. Função de busca de bugs (só criar se não existir)
DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM pg_proc 
        WHERE proname = 'search_bugs'
    ) THEN
        CREATE OR REPLACE FUNCTION search_bugs(
          query_embedding VECTOR(384),
          language_param TEXT DEFAULT NULL,
          match_threshold FLOAT DEFAULT 0.3,
          match_count INT DEFAULT 3
        )
        RETURNS TABLE(
          id BIGINT,
          error_message TEXT,
          solution TEXT,
          language TEXT,
          similarity FLOAT
        )
        LANGUAGE plpgsql
        AS $$
        BEGIN
          RETURN QUERY
          SELECT
            bk.id,
            bk.error_message,
            bk.solution,
            bk.language,
            1 - (bk.embedding <=> query_embedding) AS similarity
          FROM bugs_knowledge bk
          WHERE 1 - (bk.embedding <=> query_embedding) > match_threshold
            AND (language_param IS NULL OR bk.language = language_param)
          ORDER BY bk.embedding <=> query_embedding
          LIMIT match_count;
        END;
        $$;
    END IF;
END $$;

-- 11. Função para atualizar timestamp (só criar se não existir)
DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM pg_proc 
        WHERE proname = 'update_updated_at_column'
    ) THEN
        CREATE OR REPLACE FUNCTION update_updated_at_column()
        RETURNS TRIGGER AS $$
        BEGIN
          NEW.updated_at = NOW();
          RETURN NEW;
        END;
        $$ language 'plpgsql';
    END IF;
END $$;

-- 12. Triggers para atualizar timestamp (só criar se não existirem)
DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM pg_trigger 
        WHERE tgname = 'update_knowledge_embeddings_updated_at'
    ) THEN
        CREATE TRIGGER update_knowledge_embeddings_updated_at 
        BEFORE UPDATE ON knowledge_embeddings
        FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();
    END IF;
    
